def lookup_coords(city: str) -> tuple[float, float] | None:
    """Find city coordinates by name (supports partial match)."""
    for name, coords in CITY_COORDS.items():
        if city in name or name in city:
            return coords
    return CITY_COORDS.get(city)


def haversine_distance(lat1: float, lon1: float, lat2: float, lon2: float) -> float:
    """Calculate straight-line distance in km between two coordinates."""
    R = 6371
    dlat = math.radians(lat2 - lat1)
    dlon = math.radians(lon2 - lon1)
    a = (
        math.sin(dlat / 2) ** 2
        + math.cos(math.radians(lat1))
        * math.cos(math.radians(lat2))
        * math.sin(dlon / 2) ** 2
    )
    return 2 * R * math.asin(math.sqrt(a))
