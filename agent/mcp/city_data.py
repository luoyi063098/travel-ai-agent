def lookup_coords(city: str) -> tuple[float, float] | None:
    """
    根据城市名称查找经纬度坐标（支持模糊匹配）。

    遍历字典键，如果输入名称包含于某个键或某个键包含于输入名称，则视为匹配。
    若精确/模糊匹配均失败，返回 None 表示未找到。
    """
    for name, coords in CITY_COORDS.items():             # 遍历所有城市
        if city in name or name in city:                 # 模糊匹配：任一方向包含即匹配
            return coords                                 # 返回匹配到的 (纬度, 经度)
    return CITY_COORDS.get(city)                          # 兜底：尝试精确字典查找（理论上不会执行到，但保留作为安全回退）


def haversine_distance(lat1: float, lon1: float, lat2: float, lon2: float) -> float:
    """
    使用 Haversine 公式计算地球表面两点之间的直线距离。

    参数为两点的纬度和经度（单位：度），返回距离（单位：公里）。
    该公式假设地球为理想球体，适用于大多数地理距离估算场景。
    """
    R = 6371                                               # 地球平均半径（公里），WGS-84 椭球体近似值
    dlat = math.radians(lat2 - lat1)                       # 纬度差值转为弧度
    dlon = math.radians(lon2 - lon1)                       # 经度差值转为弧度

    # Haversine 核心公式：a = sin²(Δlat/2) + cos(lat1)·cos(lat2)·sin²(Δlon/2)
    a = (
        math.sin(dlat / 2) ** 2                            # sin(Δlat/2) 的平方
        + math.cos(math.radians(lat1))                     # cos(lat1)
        * math.cos(math.radians(lat2))                     # cos(lat2)
        * math.sin(dlon / 2) ** 2                          # sin(Δlon/2) 的平方
    )
    # 最终距离 = 2 · R · arcsin(√a)
    return 2 * R * math.asin(math.sqrt(a))                 # 返回以公里为单位的球面距离
